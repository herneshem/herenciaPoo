package interf;

public class inte implements interf {
    String marca;
    String modelo;

    public inte() {
        this.marca = marca;
        this.modelo = modelo;
    }

    public void ruedas(){
        System.out.println("Marca: "+marca);
        System.out.println("Modelo: "+modelo);
        System.out.println("Modelo: "+ruedas);

    }

    public String getMarca() {
        return marca;
    }

    public String getModelo() {
        return modelo;
    }
}
